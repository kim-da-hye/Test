package java8_project;

import java.util.Scanner;

class Phone{ //클래스 Phone 선언
	private String name;
	private String tel;
	Phone(String name,String tel){ //인수있는 메소드 선언
		this.name=name;
		this.tel=tel;
	}
	public String getName() {
		return name;
	}
	public String getTel() {
		return tel;
	}
}

public class Phonebook {
	public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	Phone phone[];
	int i;
	
	System.out.print("인원수>>");
	int num=sc.nextInt();
	phone=new Phone[num];
	for(i=0;i<phone.length; i++) { //for를 이용한 반복
		System.out.print("이름과 전화번호(이름과 번호는 빈 칸 없이 입력)>>>");
		String name=sc.next();
		String tel=sc.next();
		phone[i]=new Phone(name,tel);
	}
	System.out.println("저장되었습니다...");
	while(true) {
		System.out.print("검색할 이름>>>");
		String name=sc.next();
		for(i=0;i<num;i++) {
			if(name.equals(phone[i].getName())) { // equals() 사용
				System.out.println(name+"의 번호는"+phone[i].getTel()+"입니다");
				i--;
				break;
			}
		}
		if(name.equals("그만"))break; //그만을 치면 반복이 끝남
		if(i==num)System.out.println(name+"이 없습니다");
	}
	sc.close();
}
}
